document.getElementById('ajax-contact-form').addEventListener('submit', function(e) {
    e.preventDefault(); // Empêche le rechargement et le "saut" de page

    const form = this;
    const btn = document.getElementById('submit-btn');
    const responseDiv = document.getElementById('form-response');
    const formData = new FormData(form);

    // Désactiver le bouton pendant l'envoi
    btn.disabled = true;
    btn.innerHTML = "Envoi en cours...";

    fetch('index.php?action=contact', {
        method: 'POST',
        body: formData
    })
    .then(response => response.text())
    .then(data => {
        if (data.trim() === "success") {
            // Affichage du message de succès
            responseDiv.innerHTML = "✅ Message envoyé ! Merci de votre confiance, nous vous répondrons bientôt.";
            responseDiv.style.backgroundColor = "#d4edda";
            responseDiv.style.color = "#155724";
            responseDiv.style.display = "block";
            
            form.reset(); // Vide les champs

            // Faire disparaître le message après 5 secondes
            setTimeout(() => {
                responseDiv.style.display = "none";
            }, 5000);
        } else {
            alert("Erreur lors de l'envoi.");
        }
    })
    .finally(() => {
        btn.disabled = false;
        btn.innerHTML = '<i data-lucide="send"></i> Envoyer le message';
    });
});
